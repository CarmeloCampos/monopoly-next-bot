export function getText(
  language: MaybeOptional<Language>,
  key: string,
): string {
  const lang = language ?? DEFAULT_LANGUAGE;
  const translation = locales[lang][key];

  if (!translation) {
    warn("Translation not found", { key, language: lang });
    return key;
  }

  // Replace placeholders with actual values from environment variables
  return translation
    .replace(/{botName}/g, BOT_CONFIG.displayName)
    .replace(/{botUsername}/g, BOT_CONFIG.username)
    .replace(/{supportUsername}/g, BOT_CONFIG.supportUsername)
    .replace(/{terms_url}/g, env.TERMS_URL);
}

export function getSupportedLanguages(): readonly Language[] {
  return SUPPORTED_LANGUAGES;
}

/**
 * Get localized language name for display.
 * @param lang - Language code
 * @returns Language name in its native script
 */
export function getLanguageName(lang: Language): string {
  return LANGUAGE_NAMES[lang];
}
